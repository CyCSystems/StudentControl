    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        int row = tblClientes.getSelectedRow();
        if(row > -1){
            Persona persona = coordinador.buscarPersona(tblClientes.getValueAt(row, 0).toString());
            txtCodigo.setText(persona.getIdPersona());
            txtPNombre.setText(persona.getPNombre());
            txtSNombre.setText(persona.getSNombre());
            txtPApellido.setText(persona.getPApellido());
            txtSApellido.setText(persona.getSApelido());
            txtDireccion.setText(persona.getDireccion());            
            txtTelefono.setText(coordinador.buscarTelefono(persona.getIdPersona()));
            dcFechaNacimiento.setDate(persona.getFechaNacimiento());
            Coordinador.setSelectedValue(cbGenero, persona.getGenero());
            if ("1".equals(permisos[2]) && "1".equals(permisos[3])) {
                btnModificar.setEnabled(true);
            } else {
                btnModificar.setEnabled(false);
            }
            btnNuevo.setEnabled(false);
            btnGuardar.setEnabled(false);
            btnCancelar.setEnabled(true);
        }
    }//GEN-LAST:event_tblClientesMouseClicked


