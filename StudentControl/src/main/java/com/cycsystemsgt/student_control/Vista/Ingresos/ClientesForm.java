    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Boolean resultados = false;
        if(!txtPNombre.getText().trim().equals("") && !txtPApellido.getText().trim().equals("") &&
                !txtDireccion.getText().trim().equals("") && !txtTelefono.getText().trim().equals("") &&
                dcFechaNacimiento.getDate() != null && pat.matcher(txtTelefono.getText()).matches() == true &&
                cbGenero.getSelectedIndex() != -1){
            Integer Codigo = coordinador.generarCodigo();
            Persona persona = new Persona();
            Telefono telefono = new Telefono();
            ItemComboBox item;
            
            persona.setPNombre(txtPNombre.getText().trim());
            persona.setSNombre(txtSNombre.getText().trim());
            persona.setPApellido(txtPApellido.getText().trim());
            persona.setSApelido(txtSApellido.getText().trim());
            persona.setDireccion(txtDireccion.getText().trim());
            persona.setFechaNacimiento(dcFechaNacimiento.getDate());
            persona.setRol(5);
            item = (ItemComboBox) cbGenero.getSelectedItem();
            persona.setGenero(Integer.parseInt(item.getID()));
            telefono.setTelefono(txtTelefono.getText().trim());
            
            if(nuevo == true){
                persona.setIdPersona(String.valueOf(Codigo));
                telefono.setPersona(String.valueOf(Codigo));
                resultados = coordinador.registrarClientes(persona, telefono);
            }else if(modificar == true){
                persona.setIdPersona(txtCodigo.getText());
                telefono.setPersona(txtCodigo.getText());
                resultados = coordinador.modificarClientes(persona, telefono);
            }
            
            if(resultados == true){
                coordinador.cargarClientes();
                limpiarCampos();
                deshabilitarCampos();
                txtCodigo.setText("");
                nuevo = false;
                modificar = false;
                if ("1".equals(permisos[1])) {
                    this.btnNuevo.setEnabled(true);
                } else {
                    this.btnNuevo.setEnabled(false);
                }
                btnGuardar.setEnabled(false);
                btnModificar.setEnabled(false);
                btnCancelar.setEnabled(false);
                JOptionPane.showMessageDialog(null, "Cliente guardado exitosamente", "Student Control", JOptionPane.INFORMATION_MESSAGE);
            }
            
        }else if(txtPNombre.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Debe especificar al menos el primer nombre del cliente", "Student Control", JOptionPane.ERROR_MESSAGE);
        }else if(txtPApellido.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Debe especificar al menos el primer apellido del cliente", "Student Control", JOptionPane.ERROR_MESSAGE);
        }else if(txtDireccion.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Debe especificar la direccion del cliente", "Student Control", JOptionPane.ERROR_MESSAGE);
        }else if(txtTelefono.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Debe especificar el telefono del cliente", "Student Control", JOptionPane.ERROR_MESSAGE);
        }else if(dcFechaNacimiento.getDate() == null){
            JOptionPane.showMessageDialog(null, "Debe especificar la fecha de nacimiento del cliente", "Student Control", JOptionPane.ERROR_MESSAGE);
        }else if(pat.matcher(txtTelefono.getText()).matches() == false){
            JOptionPane.showMessageDialog(null, "Debe especificar un numero de telefono valido", "Student Control", JOptionPane.ERROR_MESSAGE);
        }else if(cbGenero.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(null, "Debe seleccionaar el genero del cliente valido", "Student Control", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

